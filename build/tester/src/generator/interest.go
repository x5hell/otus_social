func InterestRows(num int) (result []structure.Table) {
	for i := 1; i <= num; i++ {
		interestRow := structure.Interest{
			ID: structure.NullString{
				Valid:true,
				String:strconv.Itoa(i),
			},
			Name: structure.NullString{
				Valid:true,
				String: Interest(i),
			},
		}
		result = append(result, interestRow)
	}
	return result
}

